CREATE TABLE dim_cuenta(
	dim_id_cuenta NUMBER PRIMARY KEY,
	ID_CUENTA NUMBER,
	NUMERO_CUENTA NUMBER(30),
	FECHA_APERTURA DATE,
	-- Status cuenta
	ID_STATUS_CUENTA NUMBER,	
	DESCRIPCION_STATUS VARCHAR(500),
	-- tipo cuenta
	ID_TIPO_CUENTA NUMBER,
	DESCRIPCION_TIPO VARCHAR(500),
	REQUISITOS_TIPO VARCHAR(500)
);

CREATE SEQUENCE cuenta
INCREMENT BY 1
START WITH 1;

